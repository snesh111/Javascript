//switch -case

const month =3
switch (month) {
    case 1:
        console.log("jan");
        break;
    
    case 2:
        console.log("feb");
        break;
    
    case 3:
        console.log("mar");
        break;
    
    case 4:
        console.log("ap");
        break;
    
    case 5:
        console.log("may");
        break;
        
    default:
        console.log("something went wrong");
        break;
}